a = 5
b = 3

# penjumlahan
c = a + b
print("hasil a + b = ", c)

# pengurangan
c = a - b
print("hasil a - b = ", c)

# perkalian
c = a*b
print("hasil a*b = ", c)

# pembagian
c = a / b
print("hasil a/b =", c)

# pembagian pembulatan keatas
# silakan diisi bagaimana formulanya
c = round(a / b)
print("hasil pembulatan a/b =", c)


# modulus
# silakan diisi bagaimana formulanya
c = a%b
print("hasil a%b =", c)

# pangkat
# silakan diisi bagaimana formulanya
c = (a*a)
print("hasil pangkat cara 1 : a*a =", c)
c = (a**2)
print("hasil pangkat cara 2 : a**2 =", c)
